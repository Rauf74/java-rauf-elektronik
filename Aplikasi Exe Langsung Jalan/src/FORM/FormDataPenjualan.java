    private void saveBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBTActionPerformed
        int id = 0, harga_beli = 0, harga_jual = 0, harga_total = 0, saham = 0, profit = 0;

        java.util.Date tanggal_beli = (java.util.Date) this.tglTF.getDate();
        String kode = this.kodeAccTF.getText();
        String nama = this.namaAccTF.getText();
        int harga_jual1 = Integer.parseInt(hargaTF.getText());
        int stok = Integer.parseInt(stokTF.getText());
        int jumlah_beli1 = Integer.parseInt(qtyTF.getText());
        int harga_total1 = Integer.parseInt(totalTF.getText());
        
        try{
            Connection c = ClassDatabase.getkoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM barang WHERE kode_barang = '" + this.kodeAccTF.getText() + "'";
            ResultSet r = s.executeQuery(sql);
            
            while (r.next()){
                harga_beli = (r.getInt("harga_beli"));
                harga_jual = (r.getInt("harga_jual"));
                saham = (r.getInt("saham"));
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi kesalahan" + e);
        }
        
        profit = (harga_jual - harga_beli) * Integer.parseInt(qtyTF.getText());
        harga_total = jumlah_beli1 * harga_jual1;
        
        stokTF.setText(Integer.toString(stok));
        qtyTF.setText(Integer.toString(jumlah_beli1));
        String total = Integer.toString(harga_total);
        String jumlah_beli = this.qtyTF.getText();
        
        if (kodeAccTF.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Data Belum Lengkap, Kode Acc Tidak Boleh Kosong");
        } else {
            if (qtyTF.getText().isEmpty()){
                JOptionPane.showMessageDialog(this,"Data Belum Lengkap, Qty Tidak Boleh Kosong");
            }
            else {
                if (qtyTF.getText().isEmpty()){
                    JOptionPane.showMessageDialog(this,"Data Belum Lengkap, Qty Tidak Boleh Kosong");
                }
                else {
                    if (totalTF.getText().isEmpty()){
                        JOptionPane.showMessageDialog(this,"Data Belum Lengkap, Total Tidak Boleh Kosong");
                    }
                    else {
                        try {
                            Connection c = ClassDatabase.getkoneksi();
                            String sql = "INSERT INTO jualacc values (?, ?, ?, ?, ?, ?)";
                            PreparedStatement p = (PreparedStatement) c.prepareStatement(sql);
                            p.setInt(1, id);
                            p.setString(2, kode);
                            p.setString(3, jumlah_beli);
                            p.setString(4, total);
                            p.setDate(5, new java.sql.Date(tanggal_beli.getTime()));
                            p.setInt(6,profit);
                            p.executeUpdate();
                            p.close();
                            JOptionPane.showMessageDialog(this,"Sukses Tambah Data");
                        } catch (SQLException e){
                            System.out.println(e);
                        } finally {
                        }
                    }
                }
            }
            try {
                Connection c = ClassDatabase.getkoneksi();
                Statement s = c.createStatement();
                String sql = "SELECT kodea, qty, total, tgl_jual, profit FROM jualacc";
                ResultSet r = s.executeQuery(sql);
                tblAcc.setModel(DbUtils.resultSetToTableModel(r));
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
        String kodebrg = this.kodeAccTF.getText();
        
        int stok2 = Integer.parseInt(stokTF.getText());
        int jumlah_beli2 = Integer.parseInt(qtyTF.getText());
        int update_barang = 0;
        
        update_barang = stok2 - jumlah_beli2;
        saham = update_barang * harga_beli;
        
        String saham1 = Integer.toString(saham);
        String update_barang1 = Integer.toString(update_barang);
        
        try {
            Connection c = ClassDatabase.getkoneksi();
            String sql = "UPDATE barang SET stok =?, saham =? WHERE kode_barang =?";
            PreparedStatement p = (PreparedStatement) c.prepareStatement(sql);
            p.setString(3, kode);
            p.setString(1, update_barang1);
            p.setString(2, saham1);
            p.executeUpdate();
            p.close();
        } catch (SQLException e){
            System.out.println(e);
        } finally {
        }
    }//GEN-LAST:event_saveBTActionPerformed

