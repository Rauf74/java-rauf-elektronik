    private void kodeAccTFCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_kodeAccTFCaretUpdate
        try {
            Connection c=ClassDatabase.getkoneksi();
            Statement s=c.createStatement();
            String sql="SELECT * FROM barang WHERE kode_barang='" + this.kodeAccTF.getText() + "'";
            ResultSet r=s.executeQuery(sql);
            while (r.next()) {
                this.namaAccTF.setText(r.getString(2));
                this.hargaTF.setText(r.getString(4));
                this.stokTF.setText(r.getString(5));
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi kesalahan" + e);
        }
    }//GEN-LAST:event_kodeAccTFCaretUpdate

    private void qtyTFCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_qtyTFCaretUpdate
        if (qtyTF.getText().isEmpty()) {
            totalTF.setText("");
        } else {
            int harga_total = 0;
            int jumlah_beli = Integer.parseInt(qtyTF.getText());
            int harga_jual = Integer.parseInt(hargaTF.getText());
            
            harga_total = jumlah_beli * harga_jual;
            this.totalTF.setText(Integer.toString(harga_total));
        }
    }//GEN-LAST:event_qtyTFCaretUpdate


